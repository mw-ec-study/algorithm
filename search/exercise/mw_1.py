res = 0

def solution(x, y):
    if x >= N or x < 0 or y >= M or y < 0: #범위 제한
        return False  

    if array[x][y] == 0: 
        array[x][y] = 1 #확인한 곳
        solution(x+1, y) #우
        solution(x-1, y) #좌
        solution(x, y+1) #상
        solution(x, y-1) #하
        return True 
    else:
        return False

for i in range(N):
    for j in range(M):
        if solution(i, j):
            res += 1

print(res)